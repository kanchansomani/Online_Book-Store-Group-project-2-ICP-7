function clock() {
    const hrs = document.getElementById('hrs');
    const min = document.getElementById('min');
    const sec = document.getElementById('sec');
   

    const date = new Date();
    hrs.innerText = (date.getHours() < 10 ? "0" : "") + date.getHours() % 12 || 12;
    min.innerText = (date.getMinutes() < 10 ? "0" : "") + date.getMinutes();
    sec.innerText = (date.getSeconds() < 10 ? "0" : "") + date.getSeconds();
  

}
setInterval(clock, 1000);
clock();  


function toggleheart(state){
    const  heartElement = document.getElementById("Like-img");
    if(state =='Full'){
        heartElement.src = './images/index img/heart.png';
    }         

    else if (state == 'empty')
    {
        
        heartElement.src = './images/index img/empty.png';
    }         
    
}  

